package com.goutham.Creational.builder.newtry;

public class Toppings {
    public boolean pepperoni;
    public boolean chicken;
    public boolean extraCheese;
    public boolean blackOlives;
    public boolean onions;
    public boolean mushrooms;

    @Override
    public String toString() {
        return "Toppings{" +
                "pepperoni=" + pepperoni +
                ", chicken=" + chicken +
                ", extraCheese=" + extraCheese +
                ", blackOlives=" + blackOlives +
                ", onions=" + onions +
                ", mushrooms=" + mushrooms +
                '}';
    }
}
